% helper function to run dfs
solve_dfs(Start,End,Way):-
    assert(goal(End)),
    Cost is 0,
    dfs(Start,Way,[],Cost).

dfs(Goal, [Goal], _,Cost):-
    goal(Goal),
    write("Total cost = "),
    writeln(Cost).

dfs(Start, [Start|Way], Visited,Cost) :-
    road(Start, N,V),
    Cost2 is Cost+V,
    \+member(N, Visited),
    dfs(N, Way, [N|Visited],Cost2).


% bestfirst search

bestfirst(End,[[End|Way]|_],Start,[End|Way]):-
    Cost is 0,
    traverse_graph(Cost,[End|Way],Start).

bestfirst(End,[Way|Que],Start,Route):-
    extend(Way,SecWay),
    append(Que,SecWay,Que1),
    sort_list(End,Que1,SecQue),
    bestfirst(End,SecQue,Start,Route).


traverse_graph(Cost,[Goal|_],Goal):-
    write("Total distance = "),
    writeln(Cost).

traverse_graph(Cost,[First,Second|Tail],Goal):-
    road(First,Second,D),
    Cost2 is Cost+D,
    traverse_graph(Cost2,[Second|Tail],Goal).


extend([Node|Way],Way2):-
    findall([Way2,Node|Way],(road(Node,Way2,_), \+ member(Way2,Way)),Way2).


swap_list(End,[[P1|S1],[P2|S2]|Tail],[[P2|S2],[P1|S1]|Tail]):-
    heu_road(P1,End,Cost1),
    heu_road(P2,End,Cost2),
    Cost1>Cost2.

swap_list(End,[X|T],[X|V]):-
    swap_list(End,T,V).

sort_list(End,L,L1):-
    swap_list(L,L2,End), !,
    swap_list(L2,L1,End).
sort_list(End,L,L).